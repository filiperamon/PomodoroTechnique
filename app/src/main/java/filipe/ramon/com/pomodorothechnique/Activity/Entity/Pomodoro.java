package filipe.ramon.com.pomodorothechnique.Activity.Entity;


import java.io.Serializable;

/**
 * Created by ufc155.barbosa on 09/11/2015.
 */

public class Pomodoro implements Serializable {

    private int id;
    private String titulo;
    private String descricao;
    private int pomodoro;
    private int ativo;
    private int concluido;


    public void Pomodoro(){

    }

    public int getConcluido() {
        return concluido;
    }

    public void setConcluido(int concluido) {
        this.concluido = concluido;
    }

    public int getAtivo() {
        return ativo;
    }

    public void setAtivo(int ativo) {
        this.ativo = ativo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getPomodoro() {
        return pomodoro;
    }

    public void setPomodoro(int pomodoro) {
        this.pomodoro = pomodoro;
    }

}
